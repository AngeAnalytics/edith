package org.stowbase.client.objects;

/**
 * This file contains constants for converting non-metric units into metric ones.
 */
public abstract class Units {

    /**
     * Convert from foot to meter. Example:
     * 
     * <pre>
     * double meters = 40.0 * FOOT;
     * </pre>
     */
    public static final double FOOT = 0.3048;

}
